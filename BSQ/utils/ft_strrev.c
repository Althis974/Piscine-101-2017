char	*ft_strrev(char *str)
{
	int		i;
	int		n;
	int		temp;

	i = 0;
	n = 0;
	while (str[n] != '\0')
	{
		n++;
	}
	n -= 1;
	while (i < n)
	{
		temp = str[i];
		str[i] = str[n];
		str[n] = temp;
		i++;
		n--;
	}
	return (str);
}
