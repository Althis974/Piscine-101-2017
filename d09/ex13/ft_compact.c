int		ft_compact(char **tab, int length)
{
	int		j;
	int		i;
	int		k;
	int		l;
	char	**res;

	i = 0;
	k = 0;
	while (tab[i] < length)
	{
		j = 0;
		l = 0;
		if (tab[i][j] != 0)
		{
			res[k][l++] = tab[i][j++];
		}
		i++;
		k++;
	}
	return (k);
}
