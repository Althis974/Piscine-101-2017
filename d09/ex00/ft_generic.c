void	ft_generic(void)
{
	int i;
	int j;

	i = 0;
	while (i < 2)
	{
		write(1, "Tut tut", 8);
		if (j != 1)
		{
			write(1, " ; ", 3);
			j = 1;
		}
		i++;
	}
	write(1, "\n", 1);
}
