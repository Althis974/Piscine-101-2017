#include <string.h>

char	*ft_strdup(char *src)
{
	int		i;
	int		j;
	char	*str;

	if (src[0] != '\0')
		str = (char*)malloc(*src + 1);
	i = 0;
	j = 0;
	while (i < (*src + 1))
	{
		str[i] = src[j];
		i++;
		j++;
	}
	str[i] = '\0';
	return (str);
}
