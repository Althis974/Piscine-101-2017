int		ft_atoi(char *str)
{
	int		sign;
	int		i;
	long	res;

	sign = 1;
	i = 0;
	res = 0;
	while (str[i] <= 32)
		i++;
	if (str[i] == '-')
	{
		sign = -1;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = ((res + (str[i] - '0')) * 10);
		i++;
	}
	return (res / 10 * sign);
}
